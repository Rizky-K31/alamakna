create table if not exists public.coffee_types (
  id text primary key,
  name text not null,
  profile text not null,
  flavor_notes text not null,
  caffeine text not null,
  altitude text not null,
  price text not null,
  description text not null,
  image_key text,
  display_order smallint not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.coffee_type_origins (
  id bigint generated by default as identity primary key,
  coffee_type_id text not null references public.coffee_types(id) on delete cascade,
  name text not null,
  display_order smallint not null default 0,
  unique (coffee_type_id, name)
);

create table if not exists public.coffee_type_specs (
  id bigint generated by default as identity primary key,
  coffee_type_id text not null references public.coffee_types(id) on delete cascade,
  label text not null,
  value text not null,
  display_order smallint not null default 0,
  unique (coffee_type_id, label)
);

create table if not exists public.coffee_map_regions (
  id text primary key,
  name text not null,
  x_percent numeric(5,2) not null,
  y_percent numeric(5,2) not null,
  color text not null default '#373028',
  display_order smallint not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.coffee_map_region_notes (
  id bigint generated by default as identity primary key,
  region_id text not null references public.coffee_map_regions(id) on delete cascade,
  note text not null,
  display_order smallint not null default 0,
  unique (region_id, note)
);

create table if not exists public.blend_profiles (
  arabica_percent smallint primary key check (arabica_percent between 0 and 100),
  robusta_percent smallint not null check (robusta_percent between 0 and 100),
  ratio text not null,
  acidity smallint not null check (acidity between 1 and 10),
  body smallint not null check (body between 1 and 10),
  bitterness smallint not null check (bitterness between 1 and 10),
  label text not null,
  description text not null,
  display_order smallint not null default 0,
  constraint blend_profiles_total_check check (arabica_percent + robusta_percent = 100)
);

create table if not exists public.grind_sizes (
  id text primary key,
  label text not null,
  label_id text not null,
  image_key text,
  display_order smallint not null default 0
);

create table if not exists public.brew_methods (
  id text primary key,
  name text not null unique,
  image_key text,
  display_order smallint not null default 0
);

create table if not exists public.grind_size_brew_methods (
  grind_size_id text not null references public.grind_sizes(id) on delete cascade,
  brew_method_id text not null references public.brew_methods(id) on delete cascade,
  display_order smallint not null default 0,
  primary key (grind_size_id, brew_method_id)
);

create table if not exists public.origin_slides (
  id text primary key,
  name text not null,
  location_line_1 text not null,
  location_line_2 text not null default 'Indonesia',
  description text not null,
  altitude text,
  image_key text,
  display_order smallint not null default 0
);

